package calculate

func Addition(number_a, number_b int) int {
	result := number_a + number_b
	return result
}
func Substrack(number_a, number_b int) int {
	result := number_a - number_b
	return result
}
func Multiple(number_a, number_b int) int {
	result := number_a * number_b
	return result
}
func Divide(number_a, number_b float64) float64 {
	result := number_a / number_b
	return result
}
